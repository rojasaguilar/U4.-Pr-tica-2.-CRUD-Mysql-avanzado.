    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Credencial c = new Credencial(Integer.parseInt(jTextField1.getText()), jTextField2.getText(),
                jTextField3.getText(), Integer.parseInt(jTextField4.getText()));

        System.out.println(c.toString());

        boolean respuesta = driver.insertar(c);

        if (respuesta) {
            JOptionPane.showMessageDialog(this, "SE INSERTÓ!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Algo pasó!");
        }
        cargarTabla();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (renglonSeleccionado == -1) {
            JOptionPane.showMessageDialog(this,
                    "ERROR! Selecciona un renglon para borrar");
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(this,
                "Seguro que deseas elminiar");

        if (respuesta == JOptionPane.OK_OPTION) {
            String idEliminar = m.getValueAt(renglonSeleccionado, 0).toString();
            if (driver.eliminar(idEliminar)) {
                setTitle("SE ELIMINO CORRECTAMETE");
                cargarTabla();
            } else {
                JOptionPane.showMessageDialog(this,
                        "ERROR! no se pudo eliminar");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

