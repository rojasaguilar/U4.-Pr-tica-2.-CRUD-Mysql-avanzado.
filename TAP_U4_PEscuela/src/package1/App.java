    private void cargarTabla() {
        datosBD = driver.mostrarTodos();
        m.setNumRows(0);
        if (datosBD.size() == 0) {
            return;
        }

        for (int i = 0; i < datosBD.size(); i++) {
            m.addRow(datosBD.get(i));
        }
    }

    private void crearColumnas() {
        m.addColumn("NO. CONTROL");
        m.addColumn("NOMBRE");
        m.addColumn("CARRERA");
        m.addColumn("FECHA EXPEDICION");
        m.addColumn("SEMESTRE");
    }
